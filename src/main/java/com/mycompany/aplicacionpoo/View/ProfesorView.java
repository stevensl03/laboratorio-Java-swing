    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String idtexto = idProfesorJText.getText();
        
        if(idtexto == null){
            JOptionPane.showMessageDialog(null, "ID no puede esta vacio");
        }else{
            int id = Integer.parseInt(idtexto);
            Profesor p = profesorController.buscarProfesor(id);
            if(p == null){
                JOptionPane.showMessageDialog(null, "No se encontr√≥ profesor con ese id");
            }else{
             String profesor = "ID: " + p.getId() + 
                                 "\nNombre: " + p.getNombres() +
                                 "\nApellidos: " + p.getApellidos() +
                                 "\nCorreo: " + p.getEmail() +
                                 "\nTipo Contrato: " + p.getTipoContrato();

                JOptionPane.showMessageDialog(null, profesor);   
            }
            
        }
    }//GEN-LAST:event_buscarActionPerformed

