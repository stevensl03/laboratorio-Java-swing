    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String idtexto = idEstudianteJText.getText();
        if(idtexto.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "EL ID no puede estar vacio");
        }else{
            double id = Double.parseDouble(idtexto);
            Estudiante e = estudianteController.buscarEstudiante(id);
            String estudiante = "ID: " + e .getId() + 
                                 "\nCodigo: " + e.getCodigo() +
                                 "\nNombre: " + e.getNombres() +
                                 "\nEstado: " + e.isActivo() +
                                 "\nPromedio: " + e.getPromedio() +
                                 "\nID Programa: " + e.getPrograma().getId() +
                                 "\nPrograma: " + e.getPrograma().getNombre();

                JOptionPane.showMessageDialog(null, estudiante);
        }
    }//GEN-LAST:event_buscarActionPerformed

