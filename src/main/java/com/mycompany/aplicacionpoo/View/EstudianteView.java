    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        String idtexto = idEstudianteJText.getText();
        String codigotexto = codigoEstudianteJText1.getText();
        String promediotexto = promedioEstudianteJText.getText();
        String estadotexto = estadoJCombo.getSelectedItem().toString();
        String idprogramatexto = idProgramaText.getText();
        
        if(idtexto.trim().isEmpty() || codigotexto.trim().isEmpty() || promediotexto.trim().isEmpty()
                || estadotexto.trim().isEmpty() || idprogramatexto.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Los campos no pueden ser vacios ");
        }else{
            double id = Double.parseDouble(idtexto);
            double codigo = Double.parseDouble(codigotexto);
            double promedio = Double.parseDouble(promediotexto);
            boolean estado = Boolean.parseBoolean(estadotexto);
            double idPrograma = Double.parseDouble(idprogramatexto);
            
            estudianteController.agregarEstudiante(id, codigo, promedio, estado, idPrograma);
            List<Estudiante> lista = estudianteController.mostrarEstudiante();
            mostrarEstudianteEnTabla(lista);

        }
    }//GEN-LAST:event_guardarActionPerformed

