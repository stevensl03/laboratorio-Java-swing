    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String idtexto = idFacultadJText.getText();
        
        if(idtexto.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID no puede esta vacio");
        }else{
            int id = Integer.parseInt(idtexto);
            FacultadController f = new FacultadController();
            Facultad facultad = f.buscarFacultad(id);
            if(facultad == null){
                JOptionPane.showMessageDialog(null, "No se encontró facultad con ese id");
            }else{
             String facu = "ID: " + facultad.getDecano().getId() + 
                                 "\nNombre: " + facultad.getDecano().getNombres() +
                                 "\nApellidos: " + facultad.getDecano().getApellidos() +
                                 "\nCorreo: " + facultad.getDecano().getEmail() +
                                 "\nID Facultad: " + facultad.getId() +
                                 "\nFacultad: " + facultad.getNombre();

                JOptionPane.showMessageDialog(null, facu);   
            }
            
        }
    }//GEN-LAST:event_buscarActionPerformed

